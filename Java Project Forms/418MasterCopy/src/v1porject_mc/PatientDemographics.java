    private void buttonDeletePatientActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonDeletePatientActionPerformed
        // TODO add your handling code here:
        
        //author sara rodriguez 
      
        String InsertSQL = "DELETE from patienttable WHERE PatientID=?"; 
        try
        {
        //register the driver class
        //initialize
            Class.forName("com.mysql.cj.jdbc.Driver");
            //actual connect with login
            Connection con = DriverManager.getConnection(dburl, "root", "P@ssw0rd");
            PreparedStatement ps = con.prepareStatement(InsertSQL); //insert statement from above
            
             //1 = first ? from above
            ps.setString(1, PatientID.getText());
            ps.executeUpdate();
           // JOptionPane.showMessageDialog(null, "delete successfully");
            con.close(); //close connection
        }
        
        catch(SQLException e)
        {
            System.out.println("Error exceuting DELETE Operation!");
            e.printStackTrace();
        }
        catch(ClassNotFoundException e)
        {
            e.printStackTrace();
        }
        
        
        
        DefaultTableModel tblModel = (DefaultTableModel) tabledemographics.getModel();
        
        //delete row
        if(tabledemographics.getSelectedRowCount()==1)
        {
            tblModel.removeRow(tabledemographics.getSelectedRow());
        }
        else
        {
            if(tabledemographics.getRowCount()==0)
            {
                //if table is empty(no data) than display message
                JOptionPane.showMessageDialog(this, "Table is empty.");
            }
            else //if table isnt empty but user did not select a row
            {
                JOptionPane.showMessageDialog(this, "Please select a row to be deleted.");
            }
        }
        
        JOptionPane.showMessageDialog(null, "Patient data is removed.");
        
        resetTextInTableDemographics();
                                            
    }//GEN-LAST:event_buttonDeletePatientActionPerformed

