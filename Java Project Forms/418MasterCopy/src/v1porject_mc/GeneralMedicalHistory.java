        //initialize
        String dburl = "jdbc:mysql://localhost:3306/genmedhis?autoconnect=true&useSSL=false"; //connection string of the database
        String InsertSql = "Select * FROM generalmedicalhistorytable WHERE PatientID =" + PatientID.getText();
        String dbOutput;
        
        try
        {
        //register the driver class
        //initialize
            Class.forName("com.mysql.cj.jdbc.Driver");
            //actual connect with login
            Connection con = DriverManager.getConnection(dburl, "root", "P@ssw0rd");
            PreparedStatement ps = con.prepareStatement(InsertSql);
      
            Statement stmt = con.createStatement();
            ResultSet rs = stmt.executeQuery(InsertSql);
            
            ResultSetMetaData Rss = rs.getMetaData();
            c = Rss.getColumnCount();
            
            DefaultTableModel Df = (DefaultTableModel)tableGenMedHis.getModel();
            Df.setRowCount(0);
           
           //inserts into jTable column and rows
                while(rs.next())
                {
                    PatientID.setText(rs.getString("PatientID")) ;

                    Vector v2 = new Vector();
                    
                    for(int a=1; a<=c; a++)
                    {
                        v2.add(rs.getString("GeneralMedicalHistoryID"));
                        v2.add(rs.getString("BloodType"));
                    }
                    
                   
                    Df.addRow(v2);
                }
           
            con.close(); //close connection
        }
        
