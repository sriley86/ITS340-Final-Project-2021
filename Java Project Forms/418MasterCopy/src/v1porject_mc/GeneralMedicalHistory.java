    private void UpdateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateButtonActionPerformed
        // TODO add your handling code here:
        //author sharyl riley
        isNew = false;
        backGround = Color.lightGray;
        toggleEditAndBackground();
        
        
        //author sara rodriguez 
        DefaultTableModel Df = (DefaultTableModel) tableGenMedHis.getModel();
        
        int selectedRowIndex = tableGenMedHis.getSelectedRow();

      
        String InsertSQL = "UPDATE generalmedicalhistorytable SET Tobacco=?, TobaccoQuantity=?, Tobaccoduraton=?, Alcohol=?, AlcoholQuantity=?, "
                + "Alcoholduration=?, Drug=?, DrugType=?, Drugduration=?, BloodType=?, Rh=? where GeneralMedicalHistoryID=?"; 
        
        //UPDATES MYSQLDATABASE
        try
        {
            int generalMedicalHistoryID = Integer.parseInt(Df.getValueAt(selectedRowIndex,0).toString());
        //register the driver class
        //initialize
            Class.forName("com.mysql.cj.jdbc.Driver");
            //actual connect with login
            Connection con = DriverManager.getConnection(dburl, "root", "P@ssw0rd");
            PreparedStatement ps = con.prepareStatement(InsertSQL); //insert statement from above
            String tobacco = Tobacco.getText();
            String tobaccoquanity = TobaccoQuanity.getText();
            String tobaccoduration = TobaccoDuration.getText();
            String alcohol = Alcohol.getText();
            String alcoholquanity = AlcoholQuanity.getText();
            String alcoholduration = Alcoholduration.getText();
            String drug = Drug.getText();
            String drugtype = DrugType.getText();
            String drugduration = Drugduration.getText(); 
            String bloodtype = BloodType.getText();
            String rh = Rh.getText();
            
            ps.setString(1, tobacco); //1 = first ? from above
            ps.setString(2, tobaccoquanity);//2 = second ? from above
            ps.setString(3, tobaccoduration);
            ps.setString(4, alcohol);
            ps.setString(5, alcoholquanity);
            ps.setString(6, alcoholduration);
            ps.setString(7, drug);
            ps.setString(8, drugtype);
            ps.setString(9, drugduration);
            ps.setString(10, bloodtype);
            ps.setString(11, rh);
            ps.setInt(12, generalMedicalHistoryID);
            
            ps.executeUpdate();
            JOptionPane.showMessageDialog(this, "Record was Updated");
            table_update();
            //reset text
            resetTextInTableGenMed();
            
            
            con.close(); //close connection
        }
        
        catch(SQLException e)
        {
            System.out.println("Error exceuting UPDATING Operation!");
            e.printStackTrace();
        }
        catch(ClassNotFoundException e)
        {
            e.printStackTrace();
        }
        

    }//GEN-LAST:event_UpdateButtonActionPerformed

